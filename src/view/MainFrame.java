    public void equipGear(String charClass, String gearSlot, JButton btn) {
        selectedGear = null;
        gearSlotSelected = btn;
        slotEquipping = gearSlot;
        unableGearSelect(true);
        switch (gearSlot) {
            case "Weapon1":
            case "Weapon2":
                rbtDefense.setEnabled(false);
                rbtAcessory.setEnabled(false);
                rbtWeapon.setSelected(true);
                if (rbtWeapon.isSelected()) {
                    switch (charClass) {
                        case "Knight":
                        case "Mechanician":
                        case "Fighter":
                        case "Pikeman":
                        case "Archer":
                        case "Assassin":
                        case "Atalanta":
                            rbtType9.setEnabled(false);
                            rbtType9.setSelected(false);
                            rbtType10.setEnabled(false);
                            rbtType10.setSelected(false);
                            rbtType1.setSelected(true);
                            popularListaSword();
                            break;
                        case "Shaman":
                            rbtType9.setEnabled(false);
                            rbtType9.setSelected(false);
                            rbtType10.setEnabled(true);
                            rbtType10.setSelected(true);
                            popularListaPhantom();
                            break;
                        case "Magician":
                        case "Priestess":
                            rbtType9.setEnabled(true);
                            rbtType10.setSelected(false);
                            rbtType10.setEnabled(false);
                            rbtType9.setSelected(true);
                            popularListaWand();
                            break;
                    }
                }
                break;
            case "Armor":
                setGearSelectDefense();
                switch (charClass) {
                    case "Knight":
                    case "Archer":
                    case "Pikeman":
                    case "Fighter":
                    case "Mechanician":
                    case "Assassin":
                    case "Atalanta":
                        unableGearSelect(false);
                        rbtDefense.setSelected(true);
                        rbtType1.setSelected(true);
                        popularListaArmor();
                        break;
                    case "Priestess":
                    case "Shaman":
                    case "Magician":
                        unableGearSelect(false);
                        rbtDefense.setSelected(true);
                        rbtType2.setSelected(true);
                        popularListaArmor();
                        break;
                }
                break;
            case "Shield1":
            case "Shield2":
                setGearSelectDefense();
                switch (charClass) {
                    case "Knight":
                    case "Archer":
                    case "Pikeman":
                    case "Fighter":
                    case "Mechanician":
                    case "Assassin":
                    case "Atalanta":
                        unableGearSelect(false);
                        rbtDefense.setSelected(true);
                        rbtType3.setSelected(true);
                        popularListaShield();
                        break;
                    case "Priestess":
                    case "Shaman":
                    case "Magician":
                        unableGearSelect(false);
                        rbtDefense.setSelected(true);
                        rbtType3.setSelected(true);
                        popularListaShield();
                        unableGearSelect(false);
                        rbtDefense.setSelected(true);
                        rbtType4.setSelected(true);
                        popularListaOrb();
                        break;
                }
                break;
            case "Bracelet":
                setGearSelectDefense();
                unableGearSelect(false);
                rbtDefense.setSelected(true);
                rbtType5.setSelected(true);
                popularListaBracelet();
                break;
            case "Gauntlet":
                setGearSelectDefense();
                unableGearSelect(false);
                rbtDefense.setSelected(true);
                rbtType6.setSelected(true);
                popularListaGauntlet();
                break;
            case "Boots":
                setGearSelectDefense();
                unableGearSelect(false);
                rbtDefense.setSelected(true);
                rbtType7.setSelected(true);
                popularListaBoots();
                break;
            case "Sheltom":
                setGearSelectAcessory();
                unableGearSelect(false);
                rbtAcessory.setSelected(true);
                rbtType3.setSelected(true);
                popularListaSheltom();
                break;
            case "Ring1":
            case "Ring2":
                setGearSelectAcessory();
                unableGearSelect(false);
                rbtAcessory.setSelected(true);
                rbtType2.setSelected(true);
                popularListaRing();
                break;
            case "Necklace":
                setGearSelectAcessory();
                unableGearSelect(false);
                rbtAcessory.setSelected(true);
                rbtType1.setSelected(true);
                popularListaNecklace();
                break;
        }
        animGear.open(panGearSelect, true);
        panStatsFields.setVisible(false);
        btnEquip.setEnabled(true);
    }

