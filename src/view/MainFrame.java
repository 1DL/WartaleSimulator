    public void oneOrTwoHanded(){
        if (weapon1.isOneOrTwoHanded()){
            shield1 = weapon1;
        } else if (weapon2.isOneOrTwoHanded()){
            shield2 = weapon2;
        }
        
        if (shield1.getItemType().equals("Shield") || shield1.getItemType().equals("Orb")) {
            if (weapon1.isOneOrTwoHanded()){
                weapon1 = null;
            }
        } else if (shield2.getItemType().equals("Shield") || shield1.getItemType().equals("Orb")) {
            if (weapon2.isOneOrTwoHanded()){
                weapon2 = null;
            }
        }
    }
    
   
