    private void swapCurrentGear() {
        if (currentGear) {
            btnGear1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/inventory/btnLeftOn.png")));
            btnGear2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/inventory/btnRightOff.png")));
            btnWeapon2.setVisible(false);
            btnShield2.setVisible(false);
            btnWeapon1.setVisible(true);
            btnWeapon2.setVisible(true);
            currentWeapon = weapon1;
            currentShield = shield1;
            sfx.playSound(currentWeapon.getItemType()+".wav");
            sfx.playSound(currentShield.getItemType()+".wav");
            currentGear = false;
        } else {
            btnGear2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/inventory/btnRightOn.png")));
            btnGear1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/inventory/btnLeftOff.png")));
            btnWeapon1.setVisible(false);
            btnShield1.setVisible(false);
            btnWeapon2.setVisible(true);
            btnShield2.setVisible(true);
            currentWeapon = weapon2;
            currentShield = shield2;
            sfx.playSound(currentWeapon.getItemType()+".wav");
            sfx.playSound(currentShield.getItemType()+".wav");
            currentGear = true;
        }
    }
